package com.processing.authorization.services;

/**
 * Сервис для очистки устаревших записей об использовании лимитов.
 * <p>
 * Выполняет периодическое удаление данных за предыдущий месяц,
 * чтобы предотвратить неограниченный рост таблицы limit_usage.
 */
public interface CleanupServiceInterface {
    /**
     * Удаляет записи об использовании лимитов за предыдущий месяц.
     * <p>
     * Запускается автоматически по расписанию: 2-го числа каждого месяца в 00:00
     * UTC.
     * При возникновении ошибки логирует её, но не прерывает выполнение приложения.
     *
     */
    void cleanPreviousMonthRecords();
}
